// To print 1 upto 100 
# include<stdio.h>
int main(){
  for(int i=1;i<=100;i=i+1){
    printf("%d\n",i);
  }
    return 0;
}

// To print 20  upto 1
# include<stdio.h>
int main(){
  for(int i=20;i>=1;i=i-1){
    printf("%d\n",i);
  }
    return 0;
}


//To print 1  upto 10

//BY FOR LOOP
 # include<stdio.h>
int main(){
  for(int i=0;i<=10;i=i+1){
    printf("%d\n",i);
  }
    return 0;
}

//BY WHILE LOOP
#include<stdio.h>
int main(){
    int i=1;
    while(i<=10){
        printf("%d\n",i);
        i++;
    }
return 0;
}

//BY DO...WHILE LOOP
#include<stdio.h>
int main(){
    int i=1;
    do{printf("%d\n",i);
    i++;
    }while(i<=10);
return 0;
}



